package com.moa.backend.domain.makernews.entity;

/**
 * 한글 설명: 메이커 소식 유형
 * - EVENT       : 이벤트 안내 (이벤트/행사/프로모션 등)
 * - NOTICE      : 공지 사항 (배송, 휴무, 플랫폼 안내 등)
 * - NEW_PRODUCT : 신제품/신규 리워드 출시 안내
 */
public enum MakerNewsType {
    EVENT,       // 한글 설명: 이벤트
    NOTICE,      // 한글 설명: 공지
    NEW_PRODUCT  // 한글 설명: 신제품 출시
}
